#include "../push_swap.h"

void	ft_rb(t_list **b, char c)
{
	t_list	*tmp;

	if (!(*b))
		return ;
	tmp = *b;
	*b = (*b)->next;
	tmp->next = NULL;
	ft_lstadd_back(b, tmp);
	if (c)
		ft_putstr_fd("rb\n", 1);
}
