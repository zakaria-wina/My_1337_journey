#include "../push_swap.h"

void	ft_ra(t_list **a, char c)
{
	t_list	*tmp;

	if (!(*a))
		return ;
	tmp = *a;
	*a = (*a)->next;
	tmp->next = NULL;
	ft_lstadd_back(a, tmp);
	if (c)
		ft_putstr_fd("ra\n", 1);
}
