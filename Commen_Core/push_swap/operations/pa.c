#include "../push_swap.h"

void	ft_pa(t_list **a, t_list **b, char c)
{
	t_list	*tmp;

	if (!(*b))
		return ;
	tmp = *b;
	*b = (*b)->next;
	ft_lstadd_front(a, tmp);
	if (c)
		ft_putstr_fd("pa\n", 1);
}
