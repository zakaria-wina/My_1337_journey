#include "../push_swap.h"

void	ft_pb(t_list **a, t_list **b, char c)
{
	t_list	*tmp;

	if (!(*a))
		return ;
	tmp = *a;
	*a = (*a)->next;
	ft_lstadd_front(b, tmp);
	if (c)
		ft_putstr_fd("pb\n", 1);
}
