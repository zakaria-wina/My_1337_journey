int	ft_sqrt(int nb)
{
	int	i;

	if (nb < 0)
		return (0);
	if (nb < 2)
		return (nb);
	if (nb % 2)
		i = 3;
	else
		i = 2;
	while (i <= nb / i)
	{
		if (i * i == nb)
			return (i);
		i += 2;
	}
	return (0);
}
